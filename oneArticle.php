    <h4>微型3D打印，数秒内结构成型</h4>
    <div class="author">作者<output>凯瑟琳·波尔扎克（Katherine Bourzac）</output>发表于<output>2014-04-03&nbsp;&nbsp;16:55</output></div>
    <img src="h-pages/one-article.png">
    <article>
    	<p>已完成的研究用的是相对较厚的铜片——远比那些计算机芯片中可以找到的铜导线粗。不过巴拉丁预计v在更细的铜线中也存在这样的导热效应。他目前正研究粗细和商用计算机芯片导线相仿的的铜—石墨烯导线。</p>
        <p>这是个紧迫的问题。今年，英特尔公司预计将发布采用带有14纳米晶体管的产品，用的是同样尺寸甚至更细的铜导线。直径10纳米以下的铜导线不能正常工作，而可以用什么材料还不清楚。“我们还没找到能在10纳米以下工作的互连材料，”部分原因就是过热，萨罗杰·纳亚克（Saroj Nayak）说。他是纽约州特洛伊（Troy）的伦斯勒理工学院集成电子中心（Center for Integrated Electronics at the Rensselaer Polytechnic Institute）物理学家。</p>
        <p>总部设在加利福尼亚州圣克拉拉的半导体设备制造商应用材料公司的电气工程师马吉德佛德（Majeed Foad）负责帮助公司跟踪新材料研究。他表示石墨烯的特性令人兴奋，但他补充说，由于芯片元件日益小型化，它们对高温变得愈加敏感。而要制造高质量的石墨烯需要相当高的温度——巴兰丁和诺沃肖洛夫要把他们的导线加热到超过1000°C。佛德说在这么高的温度下晶体管和其他芯片元件会被分解。不过，巴兰丁指出也有实验室的研究表明，至少在实验室条件下，石墨烯可以在较低温度下生长。</p>
        <p>无论如何，佛德说，芯片制造商都不会急于接受石墨烯。“更换材料是非常痛苦的工作，所以我们会去挤压现有材料的最后一滴性能。”</p>
        <p>很明显，简单地把更多晶体管塞进处理器并把更多的处理器放进芯片中，是撑不了多久的。高端芯片内部已经有50到60千米长的铜导线以及多个核心了。</p>
        <p>半导体行业研究联盟（Semiconductor Research Corporation）位于北卡罗来纳州达勒姆的行业研究协会。它的互连研究主任乔纳森·坎德拉里亚（Jonathan Candelaria）说，增加更多的晶体管不能像过去那样提高性能了。解决之道可能仍是采用完全不同的架构。设计和封装芯片的新方法可以帮助解决发热问题，而且可给该行业争取利用新材料解决问题的时间，这些新材料中可能就包括新的石墨烯-铜混合材料，坎德拉里亚说。</p>
	</article>
